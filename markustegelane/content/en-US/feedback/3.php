<h1>Creating a moderator account</h1>
<p>If you want to become a content moderator, you have to do the following:</p>
<ol>
<li>Contact an administrator under the "Feedback" page and send the following details<br/>
	<ul>
		<li>Your social media profile names (for background check)</li>
		<li>Alternate ways to contact you (e.g. an e-mail address), in case you forget your password</li>
		<li>Reason(s), why you want to be a moderator</li>
		<li>Previous experience as a moderator</li>
		<li>Your activity on my YouTube channel, e.g. which channels you watch,
			which channels you've subscribed to, do you watch live streams, do you comment</li>
		<li>Time of day you are able to moderate</li>
	</ul></li>
<li>Wait until I respond to your message. Keep logging in using the same details
from the feedback page using the exact same details until you see my reply.</li>
<li>Copy the verification code in the reply</li>
<li>Open the settings menu (button you see in the bottom left of your screen)</li>
<li>Click "Log in" under the management account section</li>
<li>Enter a username, which is usually the one, you used to post the comment under feedback</li>
<li>Leave the password field empty</li>
<li>Click "Log in"</li>
<li>If you can't log in, contact the administrator</li>
<li>You'll see three text fields</li>
<li>Into the first field, enter the verification code, which you copied earlier</li>
<li>Into the second and third fields, enter a password, which you use to log in in the future</li>
<li>Click "Submit"</li>
<li>Log in again, using the same username and the new password that you just set</li>
<li>Congratulations, you are now a content moderator!</li>
<li>If you're having problems in the future (e.g. you forgot the password), contact the administrator under feedback</li>
</ol>
