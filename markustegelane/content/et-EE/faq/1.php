	<p><b>Miks ma ei saa faile alla laadida/vajan juurdepääsu?</b><br />
<br />
Mõned allalaaditavad failid, mis on hostitud Google Drive-is, vajavad nõusolekut üleslaadijalt. Üheks variandiks on lihtsalt nõuda juurdepääsu, kuid see pole soovitatav, sest ma ei pruugi kohe teile nõusolekut anda (võib kuluda mitu päeva). Alternatiivina saate kasutada linki minu kanali kirjelduses, mis suunab teid avalikku Google Drive kausta, mis sisaldab kõiki alla laaditavaid faile. Samuti saate kasutada sellel saidil olevaid allalaadimise linke või peegellinke.<br />
