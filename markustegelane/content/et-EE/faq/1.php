<b>Mis juhtus kanalitega MarkusTegelane kogukond ja MarkusTegelane++?</b><br />
<b><br /></b>
Mõlemad kanalid suleti vähese aktiivsuse tõttu. Kogukonna kanalil ei olnud kogukonna osalust ja programmeerimise kanalile postitasin liiga harva. MT++ ja MT kogukond ühinesid kanaliga MarkusTegelane+. Kui soovite liituda kanali MarkusTegelane+ kogukonda (suhtleja või haldurina), saatke kanalile MarkusTegelane+ privaatsõnum (kanal, teave, saada sõnum).<br />
