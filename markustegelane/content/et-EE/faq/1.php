<b>Mis toimub teie kanalite logo kujunduses?</b><br />
<b></b>
<p>Kanali logod koosnevad erinevatest osadest, et luua üks tervik. Esimene on selle värv. Minu põhikanal on sinine, sest see on värv, mis kõigile meeldib, vähemalt statistika järgi. Lisakanal on punane lilla üleminekuga. See sellepärast, et punane tähendab lähedust ja lilla uudsust. Teine põhjus värvidele tuleneb kanalite koodnimedest (mustikas, maasikas).</p><p>Teine osa on romb/pööratud ruut. Viimane sümboliseerib minu lemmikkujundit. Rombi/ruudu sees on põhikanalil laineliseks muudetud konsoolikriips ja teisel kanalil sinine x sümbol. Kujundussüsteemi koodnimi on LaserLite (homofon inglisekeelsest väljendist "laser light"). Põhikanali logo täisversiooni tekstile on lisatud ka must-valge müraefekt, teisel kanalil on müraefekt värviline.</p><br />
<b>Mis juhtus kanalitega Eesti Valdus 2015 (varem EestiNali 2015) ja ElektrooniliseRakenduseTeadus?</b><br />
<b><br /></b>
Kanal Eesti Valdus 2015 kustutati täielikult, kuna vaatajad saatsid pidevalt negatiivseid kommentaare ning meeldib/ei meeldi oli ei meeldi ülekaalus. Seetõttu ei olnud seda kanalit enam terve aasta. Aasta hiljem, aastal 2016, otsustasin selle kanal taasluua nimega Eesti Valdus (ilma aasta numbrita), kuid limiteerin nüüd selle üleslaadimise sagedust ja postitan vahepeal ka "mittekomöödia" videosid. ElektrooniliseRakednuseTeadus nimetati ümber Markuse asjadeks. Markuse asjade kanalil on kõik endised Elektroonilise Rakenduse Teaduse videod ning mõned videod ERT videote arhiivist. Nime muutmise põhjus oli segaduse vältimine. Markuse asjad sobis paremini nimeks, kuna see annab täpselt teada, milline on kanali sisu. Eesti Valdus aastast 2018 vaigistati selle aasta alguses, st videod pole enam avalikult vaadatavad, kuid kanal ikka veel eksisteerib.<br />
