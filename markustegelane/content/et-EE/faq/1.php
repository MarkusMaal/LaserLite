	<b>Miks on teie kanali nimi markustegelane?</b><br />
<br />
Kanali nimi on tulnud mu tegelase nimest teenuses Google Play mängud. Kanali nimi ise koosneb 3-st tähtsast osast. Esimene, loomulikult, on minu eesnimi (Markus). Teine on tegelane. Tegelane iseloomustab minu oselemist videotes, kui mingi tegelaskujuna. See on ka põhjus, miks ma enda nägu videotes eriti ei näita. Kolmas osa, mida võib-olla ei märgata on see, et nimes ei ole tühikuid. See on loodud meenutuseks minu varasele hüüdlausele "Markus Maal - Ühendatud". Seda ma näitasin videotes introga, kus Markus Maal oli esialgu lahku kirjutatud ja siis muutus see tekstiks MarkusMaal ja alla tuli tekst "Ühendatud".<br />
	<br/><b>Miks on teie kanali nimi kirjutatud väikeste tähtedega?</b><br />
<br />
Tegu on teadlikku liberaalse otsusega. Väikesed tähed on minu meelest lahedamad ja reeglite eiramine peegeldab taaskord kanali unikaalsust ja uuenduslikkust, samuti mugavust. Ma ei poolda kanali nime lahkukirjutamist või suur(t)e algustäh(t)e(de) kasutamist kanali nimele viidates.<br />
<br />
	<b>Miks on mu teise kanali nimi markustegelane x?</b><br />
<br />
Algne nimi oli MarkusTegelane+, kuid ma otsustasin seda muuta, kuna soovisin muuta suured tähed väikesteks, kuid Google ei võimaldanud mul seda teha (plussmärgi tõttu). Ma asendasin plussi x-ga, kahel põhjusel. Esiteks, see näeb välja nagu pööratud + märk ja teiseks, sest x pärineb sõnast "extra".
