<h1>Tagasiside</h1>
<p>Siin on võimalik saata tagasisidet ja küsimusi veebisaidi administraatorile. Nimi ja
parool saadetakse serverisse ja krüptitakse enne salvestamist. See tähendab, et peate
meelde jätma nii enda kasutajanime kui ka parooli, kui soovite näha administraatori
vastuseid Teie küsimustele. Kui unustate need andmed, peate uued sisestama. Enne nende
andmete sisestamist veenduge, et Teie ühendus oleks turvaline ning sertifikaadid
pärineksid Cloudflare'st (v.a. kui olete veebisaidi arendaja).</p>
<p>Teie sisselogimisandmed pole andmebaasis nähtavad, kuna need krüptitakse enne
andmebaasi salvestamist. See tähendab, et administraator ei saa mitte mingisugusel
viisil Teie kontosse sisse logida. Saadetud sõnumid ja sõnumites olevad hüüdnimed
on nähtavad ainult administraatorile.</p>
<p>Kasutajanimi on nähtav ainult Teile. Pärast väljumist see unustatakse ja 
taastatakse siis, kui logite uuesti sisse.</p>
<p>Siit saate lisaks tagasiside saatmisele teha ka järgnevat: </p>
<ul>
<li>Paluda andmete täielikku kustutamist andmebaasist, mida ei saa tavaliideste kaudu kustutada (k.a. need sisselogimisandmed)</li>
<li>Küsida, millised andmed Teie kohta eksisteerivad andmebaasis</li>
<li>Anda keelelist nõu/parandada tõlkimisvigu</li>
<li>Kommenteerida kasutamiskogemuse kohta</li>
<li>Veebisaidi moderaatori konto luua (<a href="?doc=feedback&s=3">juhised</a>)</li>
<li>Teavitada viirustest või katkistest linkidest</li>
</ul>

