<h1>Moderaatori konto loomine</h1>
<p>Juhul, kui soovite saada veebisaidi sisumoderaatoriks, tuleb teha järgnevat:</p>
<ol>
<li>Kontakteeruda veebisaid administraatoriga lehel "Tagasiside" ning saata järgmised andmed<br/>
	<ul>
		<li>Teie sotsiaalvõrgustike kasutajanimed (taustakontrolliks)</li>
		<li>Alternatiivsed viisid kontakteerumiseks (nt meiliaadress), juhuks kui unustate tagasiside parooli</li>
		<li>Põhjus(ed), miks soovite saada moderaatoriks</li>
		<li>Varasem kogemus moderaatorina</li>
		<li>Teie aktiivsus minu YouTube'i kanalitel, nt milliseid kanaleid vaatate,
			millised kanalid olete tellinud, kas vaatate otseülekandeid, kas kommenteerite</li>
		<li>Saadavalolev ajad modereerimiseks</li>
	</ul></li>
<li>Oodata kuni ma vastan Teie saadetud sõnumile. Logige aeg-ajalt sellel veebisaidil tagasiside lehel täpselt
samade andmetega sisse kuni näete minu vastust.</li>
<li>Kopeerige vastuses olev kinnituskood</li>
<li>Avage seadetemenüü, mille leiate ekraani alumisest vasakust nurgast</li>
<li>Klõpsake "Logi sisse" halduskonto all</li>
<li>Sisestage kasutajanimi, mis on tavaliselt see, millega postitasite kommentaari tagasisides</li>
<li>Jätke parooliväli tühjaks</li>
<li>Valige "Logi sisse"</li>
<li>Kui te ei saa sisse logida, kontakteeruge uuesti administraatoriga</li>
<li>Näete nüüd kolme tekstivälja</li>
<li>Esimesse tekstivälja sisestage kinnituskood, mille te varem kopeerisite</li>
<li>Teisse ja kolmandasse sisestage parool, millega soovite tulevikus sisse logida</li>
<li>Klõpsake nupul "Saada"</li>
<li>Logige uuesti sisse sama kasutajanime ja uue parooliga, mille te äsja seadsite</li>
<li>Palju õnne, olete nüüd sisumoderaator!</li>
<li>Kui tulevikus peaks probleeme ilmnema (nt unustasite parooli), kontakteeruge administraatoriga uuesti tagasiside lehel</li>
</ol>
